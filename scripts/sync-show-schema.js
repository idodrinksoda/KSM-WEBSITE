#!/usr/bin/env node
// Regenerate <MusicEvent> JSON-LD blocks in index.html from the live .show-card
// markup. Run from a pre-commit hook (and verified in CI) so the head schema
// always matches the page. Zero dependencies — plain Node, no npm install.
//
// Usage:
//   node scripts/sync-show-schema.js          # rewrite index.html in place
//   node scripts/sync-show-schema.js --check  # exit 1 if file would change
//
// Adding a new venue: extend VENUES below. Adding a new show: just add the
// .show-card to #shows .shows-grid — schema appears on the next run.

'use strict';

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const INDEX = path.join(ROOT, 'index.html');

const SENTINEL_START = '<!-- AUTO:SHOWS START — regenerated by scripts/sync-show-schema.js — do not edit by hand -->';
const SENTINEL_END = '<!-- AUTO:SHOWS END -->';

// Per-venue address + canonical map link. Add new venues here as needed.
const VENUES = {
  'Green Auto': {
    address: { addressLocality: 'Vancouver', addressRegion: 'BC', addressCountry: 'CA' },
    sameAs: 'https://www.google.com/maps/place/green+auto/data=!4m2!3m1!1s0x548671006d91b771:0x5af86ca349312e19'
  },
  'The Mezzanine': {
    address: {
      streetAddress: '33 West 8th Avenue',
      addressLocality: 'Vancouver',
      addressRegion: 'BC',
      addressCountry: 'CA'
    },
    sameAs: 'https://www.google.com/maps/search/33+West+8th+Avenue+Vancouver+BC'
  },
  "Lana Lou's": {
    address: {
      streetAddress: '362 Powell Street',
      addressLocality: 'Vancouver',
      addressRegion: 'BC',
      addressCountry: 'CA'
    },
    sameAs: 'https://www.google.com/maps/search/362+Powell+Street+Vancouver+BC'
  }
};

const BAND_ID = 'https://kamasutramurder.com/#band';
const TZ_OFFSET = '-07:00'; // PDT — slightly off for winter shows but consistent with prior hand-written schema
const START_HOUR = 20;      // 8 PM local default; override per-card with data-show-start="HH:MM"
const DURATION_HRS = 4;

function todayISO() {
  const d = new Date();
  const y = d.getUTCFullYear();
  const m = String(d.getUTCMonth() + 1).padStart(2, '0');
  const day = String(d.getUTCDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}

function pad2(n) { return String(n).padStart(2, '0'); }

function startDateTime(date, hour, minute) {
  return `${date}T${pad2(hour)}:${pad2(minute)}:00${TZ_OFFSET}`;
}

function endDateTime(date, startHour, startMinute) {
  const totalMinutes = startHour * 60 + startMinute + DURATION_HRS * 60;
  const endHour = Math.floor(totalMinutes / 60) % 24;
  const endMinute = totalMinutes % 60;
  const dayRollover = Math.floor(totalMinutes / 60) >= 24;
  if (!dayRollover) {
    return `${date}T${pad2(endHour)}:${pad2(endMinute)}:00${TZ_OFFSET}`;
  }
  const [y, m, d] = date.split('-').map(Number);
  const next = new Date(Date.UTC(y, m - 1, d + 1));
  const ny = next.getUTCFullYear();
  const nm = pad2(next.getUTCMonth() + 1);
  const nd = pad2(next.getUTCDate());
  return `${ny}-${nm}-${nd}T${pad2(endHour)}:${pad2(endMinute)}:00${TZ_OFFSET}`;
}

function parseCard(block) {
  const date = (block.match(/data-show-date="([^"]+)"/) || [])[1];
  if (!date) return null;

  // Optional override: data-show-start="HH:MM" (24-hour local)
  const startOverride = (block.match(/data-show-start="(\d{1,2}):(\d{2})"/) || []);
  const startHour = startOverride[1] ? Number(startOverride[1]) : START_HOUR;
  const startMinute = startOverride[2] ? Number(startOverride[2]) : 0;

  // First <span> inside .show-meta is "Venue – City, ST"
  const venueLine = (block.match(/<div class="show-meta">[\s\S]*?<span>([^<]+)<\/span>/) || [])[1];
  if (!venueLine) return null;
  const [venuePartRaw, cityPartRaw = ''] = venueLine.split(/\s*[–-]\s*/);
  const venue = venuePartRaw.trim();
  const city = cityPartRaw.trim();

  // First <img> inside the card is the poster
  const img = block.match(/<img[^>]*src="([^"]+)"[^>]*alt="([^"]*)"/);
  if (!img) return null;
  const posterSrc = img[1];
  const posterAlt = img[2];

  // Ticket URL is on .btn-tickets (optional — door/PWYC shows have no link)
  const ticket = block.match(/<a[^>]*class="btn-tickets"[^>]*href="([^"]+)"/);
  const ticketUrl = ticket ? ticket[1] : null;

  return { date, startHour, startMinute, venue, city, posterSrc, posterAlt, ticketUrl };
}

function buildEvent(card) {
  const venueDef = VENUES[card.venue];
  if (!venueDef) {
    throw new Error(
      `Unknown venue "${card.venue}" on ${card.date}. ` +
      `Add it to VENUES in scripts/sync-show-schema.js (need address + Google Maps URL).`
    );
  }
  return {
    '@context': 'https://schema.org',
    '@type': 'MusicEvent',
    name: `Kama Sutra Murder — Live at ${card.venue}`,
    startDate: startDateTime(card.date, card.startHour, card.startMinute),
    endDate: endDateTime(card.date, card.startHour, card.startMinute),
    eventStatus: 'https://schema.org/EventScheduled',
    eventAttendanceMode: 'https://schema.org/OfflineEventAttendanceMode',
    location: {
      '@type': 'MusicVenue',
      name: card.venue,
      address: Object.assign({ '@type': 'PostalAddress' }, venueDef.address),
      sameAs: venueDef.sameAs
    },
    image: card.posterSrc,
    performer: { '@id': BAND_ID },
    organizer: { '@id': BAND_ID },
    // offers is optional — only emitted when the card has a ticket link.
    ...(card.ticketUrl && {
      offers: {
        '@type': 'Offer',
        url: card.ticketUrl,
        availability: 'https://schema.org/InStock',
        priceCurrency: 'CAD',
        category: 'primary'
      }
    })
  };
}

function renderInjection(events) {
  const indent = '  ';
  if (events.length === 0) {
    return `${indent}<!-- No upcoming shows. -->\n`;
  }
  let out = '';
  for (const ev of events) {
    const pretty = JSON.stringify(ev, null, 2)
      .split('\n')
      .map(line => indent + line)
      .join('\n');
    out += `${indent}<script type="application/ld+json">\n${pretty}\n${indent}</script>\n`;
  }
  return out;
}

function run({ check }) {
  const html = fs.readFileSync(INDEX, 'utf8');

  const startIdx = html.indexOf(SENTINEL_START);
  const endIdx = html.indexOf(SENTINEL_END);
  if (startIdx < 0 || endIdx < 0 || endIdx < startIdx) {
    throw new Error(
      `Sentinels not found in index.html. Expected:\n  ${SENTINEL_START}\n  ...\n  ${SENTINEL_END}`
    );
  }

  const cardRe = /<article class="show-card"[\s\S]*?<\/article>/g;
  const blocks = html.match(cardRe) || [];
  const today = todayISO();

  const cards = blocks
    .map(parseCard)
    .filter(Boolean)
    .filter(c => c.date >= today)
    .sort((a, b) => a.date.localeCompare(b.date));

  const events = cards.map(buildEvent);
  const injection = renderInjection(events);

  const before = html.slice(0, startIdx + SENTINEL_START.length);
  const after = html.slice(endIdx);
  const newHtml = `${before}\n${injection}  ${after}`;

  if (newHtml === html) {
    console.log(`[sync-show-schema] up to date (${cards.length} upcoming show${cards.length === 1 ? '' : 's'})`);
    return 0;
  }

  if (check) {
    console.error('[sync-show-schema] index.html is OUT OF DATE — run: node scripts/sync-show-schema.js');
    return 1;
  }

  fs.writeFileSync(INDEX, newHtml);
  const summary = cards.length
    ? cards.map(c => `${c.date} ${c.venue}`).join(', ')
    : '(none)';
  console.log(`[sync-show-schema] wrote ${events.length} MusicEvent block(s): ${summary}`);
  return 0;
}

if (require.main === module) {
  const check = process.argv.includes('--check');
  try {
    process.exit(run({ check }));
  } catch (e) {
    console.error(`[sync-show-schema] ${e.message}`);
    process.exit(1);
  }
}
